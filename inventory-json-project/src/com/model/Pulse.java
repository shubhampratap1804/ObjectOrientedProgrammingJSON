package com.model;

public class Pulse {

	String name;
	long weight;
	long pricePerKg;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public long getWeight() {
		return weight;
	}

	public void setWeight(long weight) {
		this.weight = weight;
	}

	public long getPricePerKg() {
		return pricePerKg;
	}

	public void setPricePerKg(long pricePerKg) {
		this.pricePerKg = pricePerKg;
	}
}
